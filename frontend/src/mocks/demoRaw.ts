/** M1 mock 清理：对一章内容应用确定性假规则，生成"清理后"文本制造真实 diff */
export function mockCleanChapter(content: string): string {
  const AD_PATTERNS = [
    /www\./i,
    /https?:\/\//i,
    /笔趣读|起·点·中·文|首发|免费阅读|最新章节/,
    /公众号|书友会|回复\d+领取/,
    /求月票|求收藏|推荐票|作者菌|拜谢/,
    /下载app|阅读后续精彩/,
    /图片:".*?",位置:".*?"/,
  ]
  const lines = content.split('\n')
  const kept: string[] = []
  for (const line of lines) {
    if (AD_PATTERNS.some((p) => p.test(line))) continue
    // 模拟乱码修正：移除穿插在汉字之间的孤立字母数字串（如 "终o8于" → "终于"）
    const fixed = line.replace(/(?<=[一-龥])[a-zA-Z0-9]{1,3}(?=[一-龥])/g, '')
    kept.push(fixed)
  }
  // 压缩 3 个以上连续空行为 1 个
  return kept.join('\n').replace(/\n{3,}/g, '\n\n')
}
